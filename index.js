// Import from SillyTavern core
import { extension_settings, getContext, loadExtensionSettings } from "../../../extensions.js";
import { saveSettingsDebounced } from "../../../../script.js";

// Extension name MUST match folder name exactly
const extensionName = "chatbox-decorator";
const extensionFolderPath = `scripts/extensions/third-party/${extensionName}`;

const defaultSettings = {
    enabled: false,
    theme: "flowers"
};

// ‚îÄ‚îÄ‚îÄ Load saved settings into UI ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async function loadSettings() {
    extension_settings[extensionName] = extension_settings[extensionName] || {};
    if (Object.keys(extension_settings[extensionName]).length === 0) {
        Object.assign(extension_settings[extensionName], defaultSettings);
    }

    const s = extension_settings[extensionName];
    $("#chatbox_decorator_enabled").prop("checked", s.enabled);
    $("#chatbox_decorator_theme").val(s.theme);

    // Apply decoration immediately on load if enabled
    if (s.enabled) applyDecoration(s.theme);
}

// ‚îÄ‚îÄ‚îÄ Apply / remove decoration ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
function applyDecoration(theme) {
    removeDecoration();

    const themes = {
        flowers: { tl: "üå∏", tr: "üå∫", bl: "üåº", br: "üåª" },
        cats:    { tl: "üê±", tr: "üò∫", bl: "üò∏", br: "üêæ" },
        stars:   { tl: "‚≠ê", tr: "‚ú®", bl: "üí´", br: "üåü" },
        hearts:  { tl: "üíñ", tr: "üíó", bl: "üíù", br: "üíì" },
    };

    const icons = themes[theme] || themes.flowers;

    const positions = ["tl", "tr", "bl", "br"];
    positions.forEach(pos => {
        const el = document.createElement("div");
        el.className = `chatbox-deco chatbox-deco-${pos}`;
        el.textContent = icons[pos];
        el.id = `chatbox-deco-${pos}`;
        document.body.appendChild(el);
    });

    console.log(`[${extensionName}] ‚úÖ Decoration applied: ${theme}`);
}

function removeDecoration() {
    document.querySelectorAll(".chatbox-deco").forEach(el => el.remove());
}

// ‚îÄ‚îÄ‚îÄ Event handlers ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
function onEnabledChange(event) {
    const value = Boolean($(event.target).prop("checked"));
    extension_settings[extensionName].enabled = value;
    saveSettingsDebounced();

    const theme = extension_settings[extensionName].theme;
    if (value) {
        applyDecoration(theme);
    } else {
        removeDecoration();
    }
}

function onThemeChange(event) {
    const value = $(event.target).val();
    extension_settings[extensionName].theme = value;
    saveSettingsDebounced();

    if (extension_settings[extensionName].enabled) {
        applyDecoration(value);
    }
}

// ‚îÄ‚îÄ‚îÄ Boot ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
jQuery(async () => {
    console.log(`[${extensionName}] Loading...`);

    try {
        const settingsHtml = await $.get(`${extensionFolderPath}/settings.html`);
        $("#extensions_settings2").append(settingsHtml);

        $("#chatbox_decorator_enabled").on("input", onEnabledChange);
        $("#chatbox_decorator_theme").on("change", onThemeChange);

        await loadSettings();

        console.log(`[${extensionName}] ‚úÖ Loaded successfully`);
    } catch (error) {
        console.error(`[${extensionName}] ‚ùå Failed to load:`, error);
    }
});
